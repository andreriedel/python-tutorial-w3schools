# Tuple items are ordered, unchangeable, and allow duplicate values.

thistuple = ('apple', 'banana', 'cherry')
print(thistuple)
print(len(thistuple))

thistuple = ('apple',) # one item tuple
print(thistuple)

mytuple = ('abc', 34, True, 40, 'male')
print(mytuple)
print(type(mytuple))

thistuple = tuple(('apple', 'banana', 'cherry'))
print(thistuple)
